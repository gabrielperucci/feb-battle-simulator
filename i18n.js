// ── i18n engine ──────────────────────────────────────────────────────────────
let _currentLang = localStorage.getItem('warera-lang') || 'pt';

function t(key, ...args) {
    const dict = TRANSLATIONS[_currentLang] || TRANSLATIONS.pt;
    let str = dict[key] || TRANSLATIONS.pt[key] || key;
    // Replace {0}, {1}, etc.
    args.forEach((val, i) => { str = str.replace(`{${i}}`, val); });
    return str;
}

function setLanguage(lang) {
    _currentLang = lang;
    localStorage.setItem('warera-lang', lang);
    // Update static DOM elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        el.textContent = t(el.getAttribute('data-i18n'));
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });
    // Re-render dynamic content
    if (typeof updateCurrentStatsDisplay === 'function') updateCurrentStatsDisplay();
    if (typeof renderMultiStageUI === 'function') renderMultiStageUI();
    if (typeof updateHealthTrail === 'function') updateHealthTrail();
    if (typeof updateStatTooltips === 'function') updateStatTooltips();
}

